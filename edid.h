static const size_t EDID_PRESET_COUNT =
    sizeof(EDID_PRESETS) / sizeof(EDID_PRESETS[0]);

static inline int validate_edid(const unsigned char *data) {
    unsigned char sum = 0;
    for (int i = 0; i < 128; i++) sum += data[i];
    return sum == 0;
}

static inline const EDIDPreset *find_edid(unsigned int w,
                                           unsigned int h,
                                           unsigned int hz) {
    for (size_t i = 0; i < EDID_PRESET_COUNT; i++) {
        if (EDID_PRESETS[i].width   == w &&
            EDID_PRESETS[i].height  == h &&
            EDID_PRESETS[i].refresh == hz)
            return &EDID_PRESETS[i];
    }
    return NULL;
}